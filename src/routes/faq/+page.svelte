	<h1 class="mx-[350px] my-4">What is Project DESTINY?</h1>
	<p class="mx-[350px] my-4">
		Project DESTINY serves as a centralized directory for checking the uptime of websites and
		student portals of various educational institutions, offering a convenient and single point of
