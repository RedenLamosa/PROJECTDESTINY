		educators and students to check if their school's website is up.
	</p>
	<h1 class="mx-[350px] my-4">How does the website tracker work?</h1>
	<p class="mx-[350px] my-4">
		The website tracker uses a CORS proxy to verify the functionality of school websites. This proxy
		acts like a messenger, checking if the websites are open. If the response is positive, it means
		the website is working. However, if there's a problem , the website is experiencing issues.
		Essentially, the CORS proxy functions as an efficient assistant, promptly notifying users about
		the accessibility of their school's online resources.
	</p>
